class Article {
    constructor( title, content, author, publishedAt, updatedAt, category, tags, image, viewsCount, likesCount) {
      this.title = title;
      this.content = content;
      this.author = author;
      this.publishedAt = publishedAt;
      this.updatedAt = updatedAt;
      this.category = category;
      this.tags = tags;
      this.image = image;
      this.viewsCount = viewsCount;
      this.likesCount = likesCount;
    }
  }

  export default Article;
  